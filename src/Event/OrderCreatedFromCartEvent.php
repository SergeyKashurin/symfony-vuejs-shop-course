<?php

namespace App\Event;

use Symfony\Contracts\EventDispatcher\Event;

class OrderCreatedFromCartEvent extends Event
{
    /**
     * @var Order
     */
    private $order;

    /**
     * @param Order $order
     */
    public function __construct(Order $order)
    {
        $this->order = $order;
    }

    /**
     * @return Order
     */
    public function getOrder(): Order
    {
        return $this->order;
    }

}